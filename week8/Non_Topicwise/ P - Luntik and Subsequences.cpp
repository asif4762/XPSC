void ASIF()
{
    int n, cnt_zero = 0, cnt_one = 0;
    cin >> n;
    vector<int>v(n);
    for(int i = 0; i < n; i++){
        cin >> v[i];
        if(v[i] == 0){
            cnt_zero++;
        }
        if(v[i]==1){
            cnt_one++;
        }
    }
    cout << (ll)cnt_one * (1LL << cnt_zero) << endl;
}

